using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace cs_m3 {
    internal class Task1 {
        static void Main(string[] args) {

            #region Task 1

            Console.Write("Пожалуйста введите число: ");
            int a = int.Parse(Console.ReadLine());

            if (a % 2 == 0) {
                Console.WriteLine($"Число {a} четное");
            } else {
                Console.WriteLine($"Число {a} не чётное");
            }

            Console.WriteLine();
            Console.ReadKey();

            #endregion

            #region Task 2

            int cardSum = 0;

            Console.WriteLine("Приветсвтвуем в нашем казино!");
            Console.Write("Укажите количество карт у вас на руках: ");
            int cardCount = int.Parse(Console.ReadLine());

            for (int i = 1; i <= cardCount;) {
                Console.Write($"Введите номинал {i} карты (используйте значение J, Q, K и T для картинок): ");
                string value = Console.ReadLine();
                bool isInteger = int.TryParse(value, out int num);

                if (isInteger && num >= 2 && num <= 10) {
                    cardSum += num;
                    i++;
                } else {
                    switch (value) {
                        case "J":
                        case "j":
                        case "Q":
                        case "q":
                        case "K":
                        case "k":
                        case "T":
                        case "t":
                            cardSum += 10;
                            i++;
                            break;
                        default:
                            Console.WriteLine("Указан неверный номинал карты");
                            break;
                    }
                }
            }

            Console.WriteLine($"Вы набрали {cardSum} очков!");

            Console.WriteLine();
            Console.ReadKey();

            #endregion

            #region Task 3

            bool isComposite = false;
            bool isNum;
            int counter = 2;
            int number;

            do {
                Console.Write("Введите число больше единцы: ");
                isNum = int.TryParse(Console.ReadLine(), out number);
                if (!isNum || number <= 1) {
                    Console.WriteLine("Вы ввели неверное число!\n");
                }
            } while (!isNum || number <= 1);

            while (counter < number) {
                if (number % counter == 0) {
                    isComposite = true;
                    break;
                }
                counter++;
            }

            Console.WriteLine(isComposite ? $"Число {number} не является простым числом" : $"{number} простое число");
            Console.WriteLine();
            Console.ReadKey();

            #endregion

            #region Task 4

            int min = int.MaxValue;
            bool lengthIsNumber;
            int length;

            do {
                Console.Write("Укажите длинну последовательности: ");
                lengthIsNumber = int.TryParse(Console.ReadLine(), out length);
                if (!lengthIsNumber || length < 0) {
                    Console.WriteLine("Вы ввели неверное число!\n");
                }
            } while (!lengthIsNumber || length < 0);

            for (int i = 1; i <= length; i++) {
                Console.Write($"Введите {i} число и нажмите Enter: ");
                int currentNum = int.Parse(Console.ReadLine());
                if (min > currentNum) {
                    min = currentNum;
                }
            }

            Console.WriteLine($"Наименьшее число в последовательности: {min}");
            Console.WriteLine();
            Console.ReadKey();

            #endregion

            #region Task 5

            Random rand = new Random();
            int max;
            int targetNum;
            

            Console.WriteLine("Игра угадай число!");
            Console.Write("Введите случайно целое положительно число: ");
            max = int.Parse(Console.ReadLine());
            targetNum = rand.Next(1, max+1);

            Console.WriteLine($"А теперь попробуй угадать случайное число в диапозоне от 1 и до {max}, а если ты устал, то просто нажми Enter: ");

            do {
                bool play = int.TryParse(Console.ReadLine(), out int yourNum);
                if(play) {
                    if (targetNum > yourNum) {
                        Console.WriteLine($"Загаданное число больше {yourNum}");
                    } else if (targetNum < yourNum) {
                        Console.WriteLine($"Загаданное число меньше {yourNum}");
                    } else {
                        Console.WriteLine("Вы угадали!");
                        break;
                    }
                } else {
                    Console.WriteLine($"Игра окончена, загаданное число: {targetNum}");
                    break;
                }
            } while (true);



            Console.WriteLine();
            Console.ReadLine();

            #endregion

        }
    }
}
